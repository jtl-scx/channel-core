namespace JTL\SCX\Lib\Channel\RabbitMq\ManagementApi\Queues\Model;

class Queue
{
    /**
     * @var string
     */
    private $name;

    /**
     * Queue constructor.
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
